#!/usr/bin/env node
// Generates src/build-info.ts with the current build timestamp.
// Runs before `tsc` so the value is baked into the compiled output.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outFile = resolve(__dirname, '..', 'src', 'build-info.ts');

const now = new Date();
const iso = now.toISOString();

const content = `// AUTO-GENERATED by scripts/generate-build-info.js — do not edit.
export const BUILD_TIMESTAMP = '${iso}';
export const BUILD_TIMESTAMP_MS = ${now.getTime()};
`;

writeFileSync(outFile, content, 'utf8');
console.log(`[build-info] wrote ${outFile} (${iso})`);
